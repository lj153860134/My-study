public class arrayDemo1 {
    public static void main(String[] args){
        //定义一个数组
        int [] a=new int [3];
        System.out.println(a);    //打印数组名 就是输出数组的地址值
        System.out.println(a[0]);
        System.out.println(a[1]);
        System.out.println(a[2]); //数组的索引从0开始 系统会有默认值 int类型都为0
    }
}
